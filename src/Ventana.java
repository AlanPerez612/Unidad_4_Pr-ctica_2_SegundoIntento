    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try{
            String Veliminar=txtBuscar.getText().toUpperCase();
            boolean resultado = arbol.eliminar(Veliminar);
            String mensaje="Error, no se eliminó el dato.";
            if(resultado)mensaje="Se eliminó correctamente";
            lblMensaje.setText(mensaje);
            txtBuscar.setText("");
        }catch(java.lang.StringIndexOutOfBoundsException error1){
            lblMensaje.setText("Ocurrió un error inesperado.");
            showMessageDialog(this,"Revise la ruta del path.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String mensaje="El camino sí existe.",cad=txtBuscar.getText().toUpperCase();
        if(arbol.buscarNodo(cad)==null)mensaje="El camino no existe.";
        showMessageDialog(null,mensaje);
    }//GEN-LAST:event_btnBuscarActionPerformed

    
