    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        try{
            String cad=txtPathPadre.getText().toUpperCase();
            char val=txtHijo.getText().toUpperCase().charAt(0);
            boolean resultado = arbol.insertar(val, cad);
            String mensaje="Error, no se pudo insertar";
            if(resultado)mensaje="¡Se insertó correctamente!";
            lblMensaje.setText(mensaje);
            txtHijo.setText("");
        }catch(java.lang.StringIndexOutOfBoundsException error1){
            lblMensaje.setText("Ocurrió un error inesperado.");
            showMessageDialog(this,"Revise los datos a insertar.");
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

